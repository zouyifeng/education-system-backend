package com.laboratory.po.wechat;

public class Data {
	private Data_welcomeMsg welcomeMsg; 
	private Data_title title;
	private Data_type type;
	private Data_remark remark;
	public Data_welcomeMsg getWelcomeMsg() {
		return welcomeMsg;
	}
	public void setWelcomeMsg(Data_welcomeMsg welcomeMsg) {
		this.welcomeMsg = welcomeMsg;
	}
	public Data_title getTitle() {
		return title;
	}
	public void setTitle(Data_title title) {
		this.title = title;
	}
	public Data_type getType() {
		return type;
	}
	public void setType(Data_type type) {
		this.type = type;
	}
	public Data_remark getRemark() {
		return remark;
	}
	public void setRemark(Data_remark remark) {
		this.remark = remark;
	}
	
}
